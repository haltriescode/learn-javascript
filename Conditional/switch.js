// Using `switch`

const candy = 1;

switch (candy) {
    case 0:
        console.log("I have no candy :(");
        break; //stop program
    case 1:
        console.log("I have a candy :)");
        break;
    case 2:
        console.log("I have two candies :o");
        break;
    default:
        console.log("I like candy!");
        break;
}